#include "libft.h"

int	ft_isalnum(int zam)
{
	if ((zam >= 'A' && zam <= 'Z') || (zam >= 'a' && zam <= 'z')
		|| (zam >= '0' && zam <= '9'))
		return (1);
	return (0);
}
